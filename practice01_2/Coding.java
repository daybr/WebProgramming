public class Coding {
	
	
	private String system;
	private String code;
	
	Coding (String system, String code) {
		this.system = system;
		this.code = code;
	}
	
	public String getSystem() {
		return system;
	}
	
	public void setSystem(String system) {
		this.system = system;
	}
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
}

